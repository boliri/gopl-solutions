// This version of popcount works exactly the same than the original one. The only difference is the usage of a for loop
// to calculate the population count of a binary sequence.
//!+
package popcount

// pc[i] is the population count of i.
var pc [256]byte

func init() {
	for i := range pc {
		pc[i] = pc[i/2] + byte(i&1)
	}
}

func PopCountSingleExpression(x uint64) int {
	return int(pc[byte(x>>(0*8))] +
		pc[byte(x>>(1*8))] +
		pc[byte(x>>(2*8))] +
		pc[byte(x>>(3*8))] +
		pc[byte(x>>(4*8))] +
		pc[byte(x>>(5*8))] +
		pc[byte(x>>(6*8))] +
		pc[byte(x>>(7*8))])
}

func PopCountLoop(x uint64) int {
	var pop int

	maxBytes := 8

	for i:=0; i < maxBytes; i++ {
		pop += int(pc[byte(x >> (i * maxBytes))])
	}
	
	return pop
}

